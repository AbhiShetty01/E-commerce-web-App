// Filter products by category
export const getMenWatches = () => products.filter((product) => product.category === "men")
export const getWomenWatches = () => products.filter((product) => product.category === "women")
export const getChildrenWatches = () => products.filter((product) => product.category === "children")
export const getFeaturedWatches = () => products.filter((product) => product.featured)

// Get product by ID
export const getProductById = (id) => products.find((product) => product.id === Number.parseInt(id))

// Get products on offer (for demo purposes, we'll just return some products with a discount)
export const getOffersWatches = () => {
  return products.slice(0, 8).map((product) => ({
    ...product,
    originalPrice: product.price,
    price: Math.round(product.price * 0.8), // 20% discount
  }))
}

// Search products
export const searchProducts = (query) => {
  const searchTerm = query.toLowerCase()
  return products.filter(
    (product) =>
      product.name.toLowerCase().includes(searchTerm) || product.description.toLowerCase().includes(searchTerm),
  )
}
